# Metodos para Strings é como alterar ou modificar texto dentro de uma STr

mensagem = 'eu adoro filmes de terror'
print(mensagem.capitalize ()) #primeira letra maiúcula 
print(mensagem.title ()) #letra de todas as palavras maiúscula 
print(mensagem.lower ()) #mantém todas as letras minúsculas

print(mensagem.find ('adoro')) #mostra o index de letras e palavras
print(mensagem.replace ('o','e')) #encontrar e depois trocar por 
print(mensagem.replace ('filmes','séries'))





