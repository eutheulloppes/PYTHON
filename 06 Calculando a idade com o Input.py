# quando o usuário final digitar o ano em que nasceu o programa irá calcular a idade dele

# aplicação de input:

ano_nascimento = input('em que ano você nasceu?')    # para saber o ano em que ele nasceu #é uma Str
idade = 2023 - int(ano_nascimento)       # para fazer um cálculo da idade atual # é uma Int
print(idade)      # resposta final ao usuário # é uma Int

